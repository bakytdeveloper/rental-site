// Main email sending function
export const sendEmailNotification = async (type, contactData, siteData = null, additionalData = {}) => {
    try {
        const transporter = createTransporter();

        // Ğ”Ğ¾Ğ±Ğ°Ğ²Ğ»ÑĞµĞ¼ Ğ´Ğ¾Ğ¿Ğ¾Ğ»Ğ½Ğ¸Ñ‚ĞµĞ»ÑŒĞ½Ñ‹Ğµ Ğ´Ğ°Ğ½Ğ½Ñ‹Ğµ
        const dataWithExtras = {
            ...contactData,
            ...additionalData
        };

        const template = emailTemplates[type](dataWithExtras, siteData);

        const mailOptions = {
            from: {
                name: 'RentalSite Notification System',
                address: process.env.SMTP_FROM
            },
            to: type === 'adminRentalExpiring'
                ? process.env.ADMIN_NOTIFICATION_EMAIL || process.env.SMTP_FROM
                : contactData.email,
            subject: template.subject,
            html: template.html
        };

        console.log(`ğŸ“¤ Sending ${type} email...`);
        const result = await transporter.sendMail(mailOptions);
        console.log(`âœ… ${type} email sent successfully:`, result.messageId);
        return { success: true, messageId: result.messageId };

    } catch (error) {
        console.error(`âŒ Error sending ${type} email:`, error);
        return { success: false, error: error.message };
    }
};

